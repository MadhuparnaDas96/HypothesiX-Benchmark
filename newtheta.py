# ========== BUILD THETA MATRIX ==========

names = list(THETA_REFERENCE.keys())
X     = np.array([THETA_REFERENCE[name] for name in names], dtype=float)
n     = len(names)



# ========== MAHALANOBIS CLUSTER ==========

mu  = X.mean(axis=0)
cov = np.cov(X, rowvar=False) + REG * np.eye(6)

try:
    cov_inv = np.linalg.inv(cov)
except np.linalg.LinAlgError:
    cov_inv = np.linalg.pinv(cov)

def mahal_sq(x):
    d = x - mu
    return float(d @ cov_inv @ d)

d2 = np.array([mahal_sq(X[i]) for i in range(n)])


# ========== UPSILON SCORING ==========

def upsilon(idx):
    d2_c = d2[idx]
    mask = [i for i in range(n) if i != idx]
    pool = d2[mask]
    return float(np.sum(pool <= d2_c)) / len(pool)

scores = np.array([upsilon(i) for i in range(n)])


# ========== CONSOLE REPORT ==========

print(f"  {'Rank':<5} {'d²':>9}  {'Υ(C)':>7}  Name")
